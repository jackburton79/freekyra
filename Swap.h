inline uint16 swap_s(uint16 s)
{
	return ((s & 0x00FF) << 8) | ((s & 0xFF00) >> 8);
}

inline uint32 swap_l(uint32 l)
{
	return ((l & 0x000000FF) << 24) | ((l & 0x0000FF00) << 8) |
			((l & 0x00FF0000) >> 8) | ((l & 0xFF000000) >> 24);
}

