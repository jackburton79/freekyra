Game *game = new Game;

// Class Game ------------------------------------------------------------------
Game::Game()
	:	fHolding(255)
{
	memset(fState, 0, 512 * sizeof(int8));
	memset(fStorage, 255, 64 * sizeof(int8));
}
//------------------------------------------------------------------------------
Game::~Game()
{
}
//------------------------------------------------------------------------------
void Game::SetState(int32 index)
{
	printf("SetState(%d)\n", index);
	fState[index] = 1;
}
//------------------------------------------------------------------------------
void Game::ClearState(int32 index)
{
	printf("ClearState(%d)\n", index);
	fState[index] = 0;
}
//------------------------------------------------------------------------------
int32 Game::GetState(int32 index)
{
	printf("GetState(%d) returned %d\n", index, fState[index]);
	return fState[index];
}
//------------------------------------------------------------------------------
void Game::Store(int32 index, uint8 item)
{
	fStorage[index] = item;
}
//------------------------------------------------------------------------------
uint8 Game::Retrieve(int32 index)
{
	return fStorage[index];
}
//------------------------------------------------------------------------------
void Game::PickUp(int32 index)
{
	fHolding = index;
}
//------------------------------------------------------------------------------
void Game::Drop()
{
	fHolding = 255;
}
//------------------------------------------------------------------------------
int32 Game::Holding()
{
	return fHolding;
}
